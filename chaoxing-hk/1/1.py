# 利用嵌套循环打印如图所示的2种格式的九九乘法表。要求每个公式共占6位宽，其中每个乘数各占1位，乘积占2位，左对齐，同一行相邻两个公式之间间隔两位。

def print1():
    for i in range(1, 10):
        for j in range(1, i + 1):
            print(f'{j}*{i}={i*j:<2}', end=' ')
        print()


def print2():
    for i in range(1, 10):
        print(" " * 7 * (i - 1), end='')
        for j in range(i, 10):
            print(f'{i}*{j}={i*j:<2}', end=' ')
        print()


print1()
print()
print2()

